const add = function(a, b) {
    let sum = a + b;
    return sum;
}

const subtract = function(a, b) {
    let remainder = a - b;
    return remainder;
}

const multiply = function(a, b) {
    let multi = a * b;
    return multi;
}

const divide = function(a, b) {
    let quotient = a / b;
    return quotient;
}
